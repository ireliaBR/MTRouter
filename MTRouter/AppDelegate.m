- (BOOL)application:(UIApplication *)app openURL:(NSURL *)url options:(NSDictionary<UIApplicationOpenURLOptionsKey,id> *)options {
    
    [MTRouter.router pushUrl:url.absoluteString animated:YES pushNavCtrl:(UINavigationController *)self.window.rootViewController];
    return YES;
}

